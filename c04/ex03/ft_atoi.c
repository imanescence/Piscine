int	ft_atoi(char *str)
{
	int	nb;
	int	i;
	int	ng;

	nb = 0;
	i = 0;
	ng = 1;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t'
		|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
	i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		ng *= -1;
	i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
	nb *= 10;
	nb += (str[i] - '0');
	i++;
	}
	return (ng * nb);
}
