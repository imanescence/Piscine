#include <unistd.h>

int	main(int argc, char **argv)
{
	char	c;
	int		i;
	int		j;

	i = 0;
	j = 1;
	while (j < argc)
	{
		while (argv[j][i] != '\0')
		{
			c = argv[j][i];
			write(1, &c, 1);
			i++;
		}
		write(1, "\n", 1);
		j++;
		i = 0;
	}
}
