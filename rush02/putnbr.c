#include <stdio.h>
char	ft_putnbr(int nb)
{
	if (nb >= 10)
	ft_putnbr(nb / 10);
	return ('0' + nb);
}

int main()
{
	int nb = 182;
	ft_putnbr(nb);
	printf("%c", ft_putnbr(nb));
}
